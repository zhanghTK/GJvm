package math

import (
	"GJvm/instructions/base"
	"GJvm/rtda"
)

// Shift left int
type ISHL struct{ base.NoOperandsInstruction }

func (i *ISHL) Execute(frame *rtda.Frame) {
	stack := frame.OperandStack()
	v2 := stack.PopInt()
	v1 := stack.PopInt()
	s := uint32(v2) & 0x1f
	result := v1 << s
	stack.PushInt(result)
}

